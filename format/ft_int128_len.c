#include "../libft.h"

size_t		ft_int128_len(int128_t i)
{
	register size_t	len;

	len = (i < 0) ? 2 : 1;
	i = (i < 0) ? -i : i;
	while ((i /= 10) != 0)
		++len;
	return (len);
}
